function getDetectedLanguage() {
    const browserLang = (navigator.languages && navigator.languages[0]) || navigator.language || 'en';
    if (browserLang.startsWith('fr')) return 'fr';
    if (browserLang.startsWith('es')) return 'es';
    return 'en';
}

function getStoredLanguage() {
    return localStorage.getItem('preferredLanguage') || 'auto';
}

function resolveLanguage(choice) {
    return choice === 'auto' ? getDetectedLanguage() : choice;
}

function getTranslation(key, lang) {
    const parts = key.split('.');
    let value = translations[lang];
    for (const part of parts) {
        if (!value || typeof value !== 'object') return undefined;
        value = value[part];
    }
    return value;
}

function translatePage(lang, autoDetected) {
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        const text = getTranslation(key, lang);
        if (text === undefined) return;

        const attr = element.getAttribute('data-i18n-attr');
        if (attr) {
            element.setAttribute(attr, text);
        } else {
            element.textContent = text;
        }
    });

    const titleKey = `pageTitle`;
    const pageTitle = getTranslation(`${document.body.getAttribute('data-page')}.${titleKey}`, lang);
    if (pageTitle) {
        document.title = pageTitle;
    }

    const note = document.getElementById('language-note');
    if (note) {
        note.textContent = autoDetected ? getTranslation('common.detectedLanguage', lang) : getTranslation('common.activeLanguage', lang);
    }

    const label = document.querySelector('[data-i18n="common.languageLabel"]');
    if (label) {
        label.textContent = getTranslation('common.languageLabel', lang);
    }
}

function updateThemeButton(lang) {
    const themeButton = document.getElementById('theme-toggle');
    if (!themeButton) return;
    const theme = localStorage.getItem('theme') || 'dark';
    const textKey = theme === 'light' ? 'common.enableDark' : 'common.enableLight';
    themeButton.textContent = getTranslation(textKey, lang);
}

function applyTheme(theme) {
    document.body.classList.toggle('light-theme', theme === 'light');
    localStorage.setItem('theme', theme);
}

function initializeTheme(lang) {
    const theme = localStorage.getItem('theme') || 'dark';
    applyTheme(theme);
    updateThemeButton(lang);
}

function initializeLanguageControls() {
    const languageSelect = document.getElementById('language-select');
    const storedLanguage = getStoredLanguage();
    if (languageSelect) {
        languageSelect.value = storedLanguage;
        languageSelect.addEventListener('change', () => {
            localStorage.setItem('preferredLanguage', languageSelect.value);
            const effective = resolveLanguage(languageSelect.value);
            translatePage(effective, languageSelect.value === 'auto');
            updateThemeButton(effective);
        });
    }
}

function initializePage() {
    const storedLanguage = getStoredLanguage();
    const effectiveLanguage = resolveLanguage(storedLanguage);
    translatePage(effectiveLanguage, storedLanguage === 'auto');
    initializeTheme(effectiveLanguage);
    initializeLanguageControls();

    const themeToggle = document.getElementById('theme-toggle');
    if (themeToggle) {
        themeToggle.addEventListener('click', () => {
            const newTheme = document.body.classList.contains('light-theme') ? 'dark' : 'light';
            applyTheme(newTheme);
            const storedLanguage2 = getStoredLanguage();
            const effectiveLanguage2 = resolveLanguage(storedLanguage2);
            updateThemeButton(effectiveLanguage2);
        });
    }
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initializePage);
} else {
    initializePage();
}
